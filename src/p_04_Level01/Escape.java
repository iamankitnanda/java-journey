public class Escape {
    public static void main( String[]args){
          System.out.println("Hello\"CodeBagheera\""); // agar mujhe double qouttes ko he dikhana hai.
          System.out.println("Hello\\CodeBagheera\\");// agar mujhe backslash he dikhana ho tho .

        System.out.println("Hello\nCodeBagheera"); // agar hello ko ek line aur CodeBagheera ko dusre line ein likhna hai.
        System.out.println("Hello\tCodeBagheera");  // agar tab karna hai tho(matlab ek defined space)
        System.out.println("Hello\bCodeBagheera");// fist word ka last letter udd jayega
    }
}
